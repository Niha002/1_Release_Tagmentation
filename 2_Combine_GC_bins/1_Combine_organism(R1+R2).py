    for f in ['Z_WTW_S2_R1_Range 0-5.txt', 'Z_WTW_S2_R2_Range 0-5.txt']:
        with open(f, 'r') as fd:
            shutil.copyfileobj(fd, wfd)
            wfd.write("\n")
